void QUEST::initializeQuadradure(const int& n,
  const QuadratureType& quatype,
  Vector* q1_,
  Vector* w1_)  {
  
  switch (quatype) {
    case QUEST::QuadratureType::kGaussLegendre: 
      Internal::initializeGaussLegendre(n, q1_, w1_); break;
    case QUEST::QuadratureType::kGaussLobatto: 
      Internal::initializeGaussLobatto(n, q1_, w1_); break;
    case QUEST::QuadratureType::kGaussChebyshev: 
      Internal::initializeGaussChebyshev(n, q1_, w1_); break;
    default:
      QUEST_ERROR("Intializing Gauss Quadrature Wrong!! No Such Gauss Type !!");
      break;
  }
};

std::string QUEST::name(const QuadratureType& quatype)  {
  switch (quatype) {
    case QUEST::QuadratureType::kGaussLegendre: 
      return "Gauss Legendre";
    case QUEST::QuadratureType::kGaussLobatto: 
      return "Gauss Lobatto";
    case QUEST::QuadratureType::kGaussChebyshev: 
      return "Gauss Chebyshev";
    default:
      QUEST_ERROR("Intializing Gauss Quadrature Wrong!! No Such Gauss Type !!");
      return "";
  }
};
